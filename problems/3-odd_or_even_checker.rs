use std::io::{self, Write};
use std::str::FromStr;

fn read_string(prompt: &str) -> String {
    print!("{}", prompt);
    io::stdout().flush().expect("Failed to flush stdout!");

    let mut input = String::new();
    io::stdin().read_line(&mut input).expect("Failed to read!");

    input.trim().to_string()
}

fn read_number<T: FromStr>(prompt: &str) -> T {
    loop {
        let input = read_string(prompt);

        match input.parse() {
            Ok(value) => return value,
            Err(_) => {
                println!("Invalid input. Please enter a valid number.");
            }
        }
    }
}

enum NumberType {
    Odd,
    Even,
}

fn check_number_type(num: i32) -> NumberType {
    if num % 2 == 0 {
        NumberType::Even
    } else {
        NumberType::Odd
    }
}

fn print_number_type(number_type: NumberType) {
    match number_type {
        NumberType::Odd => println!("Number is Odd."),
        NumberType::Even => println!("Number is Even."),
    }
}

fn main() {
    print_number_type(check_number_type(read_number("please enter a number: ")));
}
