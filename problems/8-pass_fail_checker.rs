use std::io::{self, Write};
use std::str::FromStr;

const PASSING_MARK: i32 = 50;

fn read_string(prompt: &str) -> String {
    print!("{}", prompt);
    io::stdout().flush().expect("Failed to flush!");

    let mut input = String::new();
    io::stdin()
        .read_line(&mut input)
        .expect("Failed to read input!");

    input.trim().to_string()
}

fn read_number<T: FromStr>(prompt: &str) -> T {
    loop {
        let input = read_string(prompt);

        match input.trim().parse() {
            Ok(value) => return value,
            Err(_) => {
                println!("Invalid input. Please enter a valid number.");
            }
        }
    }
}

enum PassFail {
    Pass,
    Fail,
}

fn read_mark() -> i32 {
    loop {
        let input = read_number("Enter exam mark (0-100): ");

        match (0..=100).contains(&input) {
            true => return input,
            false => println!("âŒ Mark must be 0-100"),
        }
    }
}

fn check_mark(mark: i32) -> PassFail {
    if mark >= PASSING_MARK {
        PassFail::Pass
    } else {
        PassFail::Fail
    }
}

fn print_results(mark: i32) {
    println!("\n{}", "=".repeat(40));
    println!("ğŸ“Š EXAM RESULT");
    println!("{}", "=".repeat(40));
    println!("Mark:          {}", mark);
    println!("Passing Mark:  {}", PASSING_MARK);

    match check_mark(mark) {
        PassFail::Pass => {
            println!("Status:        âœ… ğŸ‰ You Passed!");
            println!("Note:          Passed by {} points", mark - PASSING_MARK);
        }
        PassFail::Fail => {
            println!("Status:        âŒ You Failed!");
            println!(
                "Note:          Need {} more points to pass",
                PASSING_MARK - mark
            );
        }
    }

    println!("{}", "=".repeat(40));
}

fn main() {
    println!("ğŸ¯ PASS/FAIL CHECKER");
    println!("{}\n", "â”€".repeat(25));

    let mark = read_mark();
    print_results(mark);
}
