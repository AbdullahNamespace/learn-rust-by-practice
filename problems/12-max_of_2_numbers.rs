// ======================

fn max_of_2_numbers(number1: i32, number2: i32) -> i32 {
    if number1 >= number2 {
        number1
    } else {
        number2
    }
}

// ======================
//     OUTPUT FUNCTIONS
// ======================

fn print_result(number1: i32, number2: i32) {
    println!(
        "The Maximum Number is: {}",
        max_of_2_numbers(number1, number2)
    );
}

