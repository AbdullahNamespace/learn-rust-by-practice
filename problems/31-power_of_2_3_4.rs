use std::io::{self, Write};

// ======================
//     INPUT FUNCTIONS
// ======================

fn read_number(prompt: &str) -> i32 {
    read_number_generic::<i32>(prompt)
}

// ======================
//   PROCESSING FUNCTIONS
// ======================

fn power_of_2_3_4(num: i32) -> (i64, i64, i64) {
    let n = num as i64;
    (n.pow(2), n.pow(3), n.pow(4))
}

// ======================
//     OUTPUT FUNCTIONS
// ======================

fn print_result(num: i32) {
    let (p2, p3, p4) = power_of_2_3_4(num);

    println!("{} {} {}", p2, p3, p4);

    println!("\nâ•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•");
    println!("        POWERS CALCULATION");
    println!("â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•");
    println!("Number:      {}", num);
    println!("{}Â² = {}", num, p2);
    println!("{}Â³ = {}", num, p3);
    println!("{}â´ = {}", num, p4);
}

// ======================
//         MAIN
// ======================

fn main() {
    print_result(read_number("Please enter a number: "));
}

// ======================
//   UTILITY FUNCTIONS
// ======================

fn read_string(prompt: &str) -> Result<String, io::Error> {
    print!("{prompt}");
    io::stdout().flush()?;

    let mut input = String::new();
    io::stdin().read_line(&mut input)?;

    Ok(input.trim().to_string())
}

fn read_number_generic<T: std::str::FromStr>(prompt: &str) -> T {
    loop {
        let input = match read_string(prompt) {
            Ok(value) => value,
            Err(_) => {
                print_error("invalid input, I/O");
                continue;
            }
        };

        match input.trim().parse() {
            Ok(value) => return value,
            Err(_) => {
                print_error("invalid input, please enter a number");
            }
        }
    }
}

fn print_error(prompt: &str) {
    println!("Error: {prompt}!");
}
