use std::io::{self, Write};

// ======================
//     INPUT FUNCTIONS
// ======================

fn read_positive_number(prompt: &str) -> i32 {
    loop {
        let input = read_number::<i32>(prompt);

        if input > 0 {
            return input;
        }

        print_error("invalid input, please enter positive number");
    }
}

// ======================
//   PROCESSING FUNCTIONS
// ======================

fn factorial(number: i32) -> i64 {
    if number <= 1 {
        return 1i64;
    }

    let mut factorial = 1i64;

    for i in 2..=number {
        factorial *= i as i64;
    }

    factorial
}

