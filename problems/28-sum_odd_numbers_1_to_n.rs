#[derive(PartialEq)]
enum OddEven {
    Odd,
    Even,
}

// ======================
//     INPUT FUNCTIONS
// ======================

fn read_positive_number(prompt: &str) -> i32 {
    loop {
        let input = read_number::<i32>(prompt);

        if input > 0 {
            return input;
        }

        print_error("invalid input, please enter a positive number");
    }
}

// ======================
//   PROCESSING FUNCTIONS
// ======================

fn check_odd_or_even(number: i32) -> OddEven {
    if number % 2 == 0 {
        OddEven::Even
    } else {
        OddEven::Odd
    }
}

fn sum_odd_using_while(n: i32) -> i32 {
    if n <= 0 {
        return 0;
    }

    let mut sum = 0;
    let mut counter = 1;

    while counter <= n {
        if check_odd_or_even(counter) == OddEven::Odd {
            sum += counter;
        }
        counter += 1;
    }

    sum
}

fn sum_odd_using_loop(n: i32) -> i32 {
    // other
    // let count = (n + 1) / 2;
    // // Ù…Ø¬Ù…ÙˆØ¹ Ø§Ù„Ø£Ø±Ù‚Ø§Ù… Ø§Ù„ÙØ±Ø¯ÙŠØ© = countÂ²
    // count * count

    if n <= 0 {
        return 0;
    }

    let mut counter = 1;
    let mut sum = 0;

    loop {
        if counter > n {
            break;
        }

        if check_odd_or_even(counter) == OddEven::Odd {
            sum += counter;
        }

        counter += 1;
    }

    sum
}

fn sum_odd_using_for(n: i32) -> i32 {
    if n <= 0 {
        return 0;
    }

    let mut sum = 0;
    for i in 1..=n {
        if check_odd_or_even(i) == OddEven::Odd {
            sum += i;
        }
    }

    sum
}

