use std::{
    io::{self, Write},
    str::FromStr,
};

fn read_string(prompt: &str) -> String {
    print!("{}", prompt);
    io::stdout().flush().expect("Failed to flush stdout!");

    let mut input = String::new();
    io::stdin()
        .read_line(&mut input)
        .expect("Failed to read input!");

    input.trim().to_string()
}

fn read_number<T: FromStr>(prompt: &str) -> T {
    loop {
        let input = read_string(prompt);

        match input.trim().parse() {
            Ok(value) => return value,
            Err(_) => {
                println!("Invalid input. Please enter a valid number.");
            }
        }
    }
}

fn read_numbers() -> (i32, i32, i32) {
    let input1 = read_number("Please enter your Number 1?");
    let input2 = read_number("Please enter your Number 2?");
    let input3 = read_number("Please enter your Number 3?");

    (input1, input2, input3)
}

fn sum_of_3_numbers(num1: i32, num2: i32, num3: i32) -> i32 {
    num1 + num2 + num3
}

fn print_results(total: i32) {
    println!("The total sum of numbers is: {}", total);
}

fn main() {
    let (n1, n2, n3) = read_numbers();
    let total = sum_of_3_numbers(n1, n2, n3);
    print_results(total);
}
