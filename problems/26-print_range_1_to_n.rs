use std::io::{self, Write};

// ======================
//     INPUT FUNCTIONS
// ======================

fn read_positive_number(prompt: &str) -> i32 {
    loop {
        let input = read_number::<i32>(prompt);

        if input > 0 {
            return input;
        }

        println!("Error: invalid input, please enter a positive number!");
    }
}

// ======================
//   PROCESSING FUNCTIONS
// ======================

// ======================
//     OUTPUT FUNCTIONS
// ======================

fn print_range_using_while(n: i32) {
    println!("Using while loop:");
    let mut counter = 1;
    while counter <= n {
        print!("{} ", counter);
        counter += 1;
    }
    println!();
}

fn print_range_using_loop(n: i32) {
    println!("Using loop with break:");
    let mut counter = 1;
    loop {
        if counter > n {
            break;
        }
        print!("{} ", counter);
        counter += 1;
    }
    println!();
}

fn print_range_using_for(n: i32) {
    println!("Using for loop:");
    for i in 1..=n {
        print!("{} ", i);
    }
    println!();
}

fn print_result(n: i32) {
    print_range_using_while(n);
    print_range_using_loop(n);
    print_range_using_for(n);
}
// ======================
//         MAIN
// ======================

fn main() {
    print_result(read_positive_number("Please enter number:"));
}

// ======================
//   UTILITY FUNCTIONS
// ======================

fn read_string(prompt: &str) -> Result<String, io::Error> {
    print!("{}", prompt);
    io::stdout().flush()?;

    let mut input = String::new();
    io::stdin().read_line(&mut input)?;

    Ok(input.trim().to_string())
}

fn read_number<T: std::str::FromStr>(prompt: &str) -> T {
    loop {
        let input = match read_string(prompt) {
            Ok(value) => value,
            Err(_) => {
                println!("Error: invalid input I/O!");
                continue;
            }
        };

        match input.trim().parse() {
            Ok(value) => return value,
            Err(_) => {
                println!("Error: invalid input, please enter a valid number!");
            }
        }
    }
}
