use std::io::{self, Write};

fn read_string(prompt: &str) -> String {
    print!("{}", prompt);
    io::stdout().flush().expect("Failed to flush!");

    let mut input = String::new();
    io::stdin()
        .read_line(&mut input)
        .expect("Failed to read input!");

    input.trim().to_string()
}

fn read_boolean(prompt: &str) -> bool {
    loop {
        let input = read_string(&format!("{} (1 for Yes, 0 for No): ", prompt));

        match input.trim().to_lowercase().as_str() {
            "yes" | "y" | "t" | "true" | "1" => return true,
            "no" | "n" | "f" | "false" | "0" => return false,
            _ => {
                println!("Please enter 'y' for yes or 'n' for no.");
            }
        }
    }
}

struct PersonInfo {
    first_name: String,
    last_name: String,
}

fn read_valid_name(prompt: &str) -> String {
    loop {
        let input = read_string(prompt);

        if input.is_empty() {
            println!("âš ï¸  Warning: Name should not be empty!");
        } else {
            return input;
        }
    }
}

fn read_person_info() -> PersonInfo {
    let input_first_name = read_valid_name("Please enter first name: ");
    let input_last_name = read_valid_name("Please enter last name: ");

    PersonInfo {
        first_name: input_first_name,
        last_name: input_last_name,
    }
}

fn get_full_name(person_info: &PersonInfo, reversed: bool) -> String {
    if reversed {
        format!("{} {}", person_info.last_name, person_info.first_name)
    } else {
        format!("{} {}", person_info.first_name, person_info.last_name)
    }
}

fn print_full_name(full_name: &str) {
    println!("full name is {}", full_name);
}

fn main() {
    print_full_name(&get_full_name(
        &read_person_info(),
        read_boolean("Do you want to reverse name order"),
    ));
}
