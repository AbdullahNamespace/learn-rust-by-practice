use std::{
    io::{self, Write},
    str::FromStr,
};

fn read_string(prompt: &str) -> String {
    print!("{}", prompt);
    io::stdout().flush().expect("Failed to flush!");

    let mut input = String::new();
    io::stdin()
        .read_line(&mut input)
        .expect("Failed to read input!");

    input.trim().to_string()
}

fn read_number<T: FromStr>(prompt: &str) -> T {
    loop {
        let input = read_string(prompt);

        match input.trim().parse() {
            Ok(value) => return value,
            Err(_) => {
                println!("Invalid input. Please enter a valid number.");
            }
        }
    }
}

fn read_mark(prompt: &str) -> i32 {
    loop {
        let input = read_number(prompt);

        match (0..=100).contains(&input) {
            true => return input,
            false => println!("âŒ Mark must be 0-100!"),
        }
    }
}

fn read_numbers() -> (i32, i32, i32) {
    let input1 = read_mark("Please enter Mark 1? ");
    let input2 = read_mark("Please enter Mark 2? ");
    let input3 = read_mark("Please enter Mark 3? ");

    (input1, input2, input3)
}

fn sum_of_3_marks(mark1: i32, mark2: i32, mark3: i32) -> i32 {
    mark1 + mark2 + mark3
}

fn calculate_average(mark1: i32, mark2: i32, mark3: i32) -> f32 {
    sum_of_3_marks(mark1, mark2, mark3) as f32 / 3.0
}

fn print_results(average: f32) {
    println!("The average is: {}", average);
}

fn main() {
    let (m1, m2, m3) = read_numbers();
    let avg = calculate_average(m1, m2, m3);
    print_results(avg);
}
