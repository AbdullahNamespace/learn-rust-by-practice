use std::io::{self, Write};

// ======================
//     INPUT FUNCTIONS
// ======================

fn read_age() -> i32 {
    read_number::<i32>("Please enter your age: ")
}

// ======================
//   PROCESSING FUNCTIONS
// ======================

fn validate_number_in_range(number: i32, from: i32, to: i32) -> bool {
    number >= from && number <= to
}

fn validate_age_in_range(age: i32) -> bool {
    validate_number_in_range(age, 18, 45)
}

// ======================
//     OUTPUT FUNCTIONS
// ======================

fn print_result(age: i32) {
    if validate_age_in_range(age) {
        println!("{} is a valid age", age);
    } else {
        println!("{} is an invalid age", age);
    }
}

// ======================
//         MAIN
// ======================

fn main() {
    print_result(read_age());
}

// ======================
//   UTILITY FUNCTIONS
// ======================

fn read_string(prompt: &str) -> Result<String, io::Error> {
    print!("{}", prompt);
    io::stdout().flush()?;

    let mut input = String::new();
    io::stdin().read_line(&mut input)?;

    Ok(input.trim().to_string())
}

fn read_number<T: std::str::FromStr>(prompt: &str) -> T {
    loop {
        let input = match read_string(prompt) {
            Ok(value) => value,
            Err(_) => {
                println!("Error: invalid input I/O!");
                continue;
            }
        };

        match input.trim().parse() {
            Ok(value) => return value,
            Err(_) => {
                println!("Error: invalid input, please enter a valid number!");
            }
        }
    }
}
